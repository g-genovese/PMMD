# ====================================================================================
# MPI helpers (serial when unavailable or size==1)
# ====================================================================================
def get_comm(use_mpi):
    if not use_mpi:
        return None, 0, 1
    try:
        from mpi4py import MPI
        c = MPI.COMM_WORLD
        return c, c.Get_rank(), c.Get_size()
    except Exception as e:
        sys.stderr.write("MPI requested but mpi4py unavailable (%s); serial.\n" % e)
        return None, 0, 1


